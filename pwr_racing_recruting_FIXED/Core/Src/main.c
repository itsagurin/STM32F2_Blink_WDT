/**
  * @brief  Timer period elapsed callback function.
  * @note   This function is declared as 'weak' in the HAL drivers.
  * We redefine it here to implement our own logic.
  * It is automatically called by the timer's interrupt handler.
  * @param  htim: pointer to the timer handle structure that caused the interrupt.
  * @retval None
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  // Check if the interrupt is from our timer (TIM2).
  // This is good practice when using multiple timers.
  if (htim->Instance == TIM2)
  {
    /**
     * @brief  Toggle the state of pin PA5 (LED_PIN).
     * The HAL_GPIO_TogglePin function inverts the current logical state of the pin.
     * If it was high (LED on), it will be set to low (LED off), and vice versa.
     * Since the interrupt occurs every 100ms (10Hz), the LED will blink at 5Hz.
     */
    HAL_GPIO_TogglePin(LED_PIN_GPIO_Port, LED_PIN_Pin);
  }
}
